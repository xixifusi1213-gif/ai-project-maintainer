import assert from "node:assert/strict";
import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import test from "node:test";

import { runCli } from "../ai-project-maintainer/scripts/cli.mjs";
import { buildRepairPack, writeRepairPackFiles } from "../ai-project-maintainer/scripts/lib/repair-pack.mjs";
import { runRepairPack } from "../ai-project-maintainer/scripts/repair-pack.mjs";

function tempDir(prefix = "apm-repair-pack-") {
  return fs.mkdtempSync(path.join(os.tmpdir(), prefix));
}

function baseReport(overrides = {}) {
  return {
    schemaVersion: 1,
    root: "E:\\project",
    mode: { strict: true, release: true, production: true, agentRisk: true, profile: "auto" },
    overallStatus: "FAIL",
    passed: false,
    checks: [],
    blockers: [],
    warnings: [],
    coverageGaps: [],
    audit: { coverageGaps: [], userDecisions: [] },
    agentRisk: { findings: [], coverageGaps: [] },
    exceptions: { invalid: [] },
    ...overrides,
  };
}

test("repair pack turns blockers into auto-fix tasks", () => {
  const report = baseReport({
    checks: [
      {
        checkId: "electron-ipc",
        group: "electron",
        name: "Electron IPC",
        status: "fail",
        blocking: true,
        summary: "preload exposes broad file read",
        file: "preload.js",
      },
    ],
  });

  const pack = buildRepairPack(report, { reportPath: "reports/security-report.json", projectRoot: "E:\\project" });

  assert.equal(pack.summary.total, 1);
  assert.equal(pack.tasks[0].type, "auto_fix_candidate");
  assert.equal(pack.tasks[0].severity, "P2");
  assert.deepEqual(pack.tasks[0].targetFiles, ["preload.js"]);
  assert.match(pack.tasks[0].fixStrategy, /Electron/);
});

test("repair pack turns GAP and USER_DECISION into maintainer decision tasks", () => {
  const report = baseReport({
    overallStatus: "PASS_WITH_GAPS",
    passed: true,
    checks: [
      {
        checkId: "observability-errors",
        group: "production-audit",
        name: "Error monitoring",
        status: "GAP",
        coverageGap: true,
        summary: "No error monitoring evidence",
      },
      {
        checkId: "business-flows",
        group: "production-audit",
        name: "Business flows",
        status: "USER_DECISION",
        summary: "Maintainer must confirm critical flows",
      },
    ],
  });

  const pack = buildRepairPack(report, { reportPath: "reports/security-report.json", projectRoot: "E:\\project" });

  assert.equal(pack.summary.total, 2);
  assert.deepEqual(new Set(pack.tasks.map((task) => task.type)), new Set(["needs_maintainer_decision"]));
  assert.equal(pack.tasks.every((task) => task.userDecisionRequired), true);
});

test("repair pack ignores passed and N/A checks", () => {
  const report = baseReport({
    overallStatus: "PASS",
    passed: true,
    checks: [
      { checkId: "tests", group: "tests", name: "tests", status: "pass" },
      { checkId: "agent-risk", group: "agent-risk", name: "agent risk", status: "N/A" },
    ],
  });

  const pack = buildRepairPack(report, { reportPath: "reports/security-report.json", projectRoot: "E:\\project" });

  assert.equal(pack.summary.total, 0);
  assert.deepEqual(pack.recheckCommands, []);
});

test("repair pack includes invalid exceptions as repair tasks", () => {
  const report = baseReport({
    exceptions: {
      invalid: [
        {
          id: "expired-risk-acceptance",
          check: "package-audit",
          reason: "expired",
        },
      ],
    },
  });

  const pack = buildRepairPack(report, { reportPath: "reports/security-report.json", projectRoot: "E:\\project" });

  assert.equal(pack.summary.total, 1);
  assert.equal(pack.tasks[0].type, "auto_fix_candidate");
  assert.equal(pack.tasks[0].source.group, "exceptions");
  assert.match(pack.tasks[0].title, /expired-risk-acceptance/);
});

test("repair pack writes all agent and compatibility files", () => {
  const out = tempDir();
  const pack = buildRepairPack(baseReport({
    checks: [{ checkId: "test", group: "tests", name: "tests", status: "fail", blocking: true }],
  }), { reportPath: "reports/security-report.json", projectRoot: "E:\\project" });

  const files = writeRepairPackFiles(pack, out);
  const agentTasks = JSON.parse(fs.readFileSync(files.agentTasks, "utf8"));
  const codexTasks = JSON.parse(fs.readFileSync(files.codexTasks, "utf8"));

  for (const file of Object.values(files)) assert.equal(fs.existsSync(file), true);
  assert.equal(agentTasks.tasks.length, 1);
  assert.equal(codexTasks.targetAgent, "codex");
  assert.match(fs.readFileSync(files.fixPlan, "utf8"), /AI Agent Repair Pack/);
  assert.match(fs.readFileSync(files.powershell, "utf8"), /Generated by ai-project-maintainer/);
  assert.match(fs.readFileSync(files.shell, "utf8"), /set -eu/);
});

test("repair pack redacts credentials from JSON, Markdown, and scripts", () => {
  const out = tempDir();
  const pack = buildRepairPack(baseReport({
    checks: [
      {
        checkId: "curl-check",
        group: "ci-security",
        name: "token check",
        status: "fail",
        blocking: true,
        summary: "Authorization: Bearer SECRET123456",
        command: "curl -H \"Authorization: Bearer SECRET123456\" https://example.com?password=hunter2&dsn=postgres://u:p@db",
        recommendation: "Remove token=SECRET123456 and api_key=SECRET123456",
      },
    ],
  }), { reportPath: "reports/security-report.json", projectRoot: "E:\\project" });

  const files = writeRepairPackFiles(pack, out);
  const combined = Object.values(files).map((file) => fs.readFileSync(file, "utf8")).join("\n");

  assert.doesNotMatch(combined, /SECRET123456/);
  assert.doesNotMatch(combined, /Authorization/i);
  assert.doesNotMatch(combined, /Bearer/i);
  assert.doesNotMatch(combined, /hunter2/);
  assert.doesNotMatch(combined, /postgres:\/\/u:p@db/);
});

test("repair-pack command writes output files from a report path", () => {
  const root = tempDir();
  const reports = path.join(root, "reports");
  fs.mkdirSync(reports, { recursive: true });
  const reportPath = path.join(reports, "security-report.json");
  fs.writeFileSync(reportPath, JSON.stringify(baseReport({
    checks: [{ checkId: "semgrep", group: "sast", name: "semgrep", status: "warn", summary: "static finding" }],
  })));

  const result = runRepairPack(reportPath, { projectRoot: root, outputDir: reports });

  assert.equal(result.repairPack.summary.total, 1);
  assert.equal(fs.existsSync(path.join(reports, "agent-tasks.json")), true);
  assert.equal(fs.existsSync(path.join(reports, "codex-tasks.json")), true);
});

test("CLI repair-pack dispatches and writes files", () => {
  const root = tempDir();
  const reports = path.join(root, "reports");
  fs.mkdirSync(reports, { recursive: true });
  const reportPath = path.join(reports, "security-report.json");
  fs.writeFileSync(reportPath, JSON.stringify(baseReport({
    checks: [{ checkId: "actionlint", group: "ci-security", name: "actionlint", status: "fail", blocking: true }],
  })));

  let stdout = "";
  let stderr = "";
  const code = runCli(["repair-pack", reportPath, "--project", root, "--output", reports], {
    stdout: { write: (chunk) => { stdout += chunk; } },
    stderr: { write: (chunk) => { stderr += chunk; } },
  });

  assert.equal(code, 0);
  assert.match(stdout, /AI Agent Repair Pack generated: 1 task/);
  assert.equal(stderr, "");
  assert.equal(fs.existsSync(path.join(reports, "recheck-commands.ps1")), true);
});
